class StarTriRev1 {
    public static void main(String[] args) {
		int star = 6;
       	for(int i = 1; i<=5; i++){
			star--;
		for(int k = 1; k <= star  -1; k++){
		System.out.print(" ");
		}
		for(int j = 1; j<= i; j++){
			System.out.print("*");
			
						
		}
		System.out.println();
				
	}	
    }
}
